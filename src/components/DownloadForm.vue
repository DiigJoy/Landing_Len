import { ref, reactive, computed, onMounted } from "vue";

import { getPrimaryResource, Resource } from "../services/contentService";

// Recurso a descargar
const resource = ref<Resource | null>(null);
const pdfUrl = computed(
  () => resource.value?.pdfFile || "/recursos/ebook-ejemplo.pdf"
);

// Cargar el recurso principal
onMounted(async () => {
  resource.value = await getPrimaryResource();
});

// Estado del formulario
const form = reactive({
  name: "",
  email: "",
  company: "",
  consent: false,
});

const isSubmitting = ref(false);
const showSuccess = ref(false);

// Manejar envío del formulario
const handleSubmit = async () => {
  isSubmitting.value = true;

  try {
    // Preparamos los datos para enviar a Netlify
    const formData = new FormData();
    formData.append("form-name", "download-form");
    formData.append("name", form.name);
    formData.append("email", form.email);
    formData.append("company", form.company);
    formData.append("consent", form.consent ? "yes" : "no");

    // Enviamos el formulario a Netlify
    const response = await fetch("/", {
      method: "POST",
      headers: {
        "Content-Type": "application/x-www-form-urlencoded",
      },
      body: new URLSearchParams(formData as any).toString(),
    });

    if (response.ok) {
      // Mostrar mensaje de éxito y resetear formulario
      showSuccess.value = true;
      form.name = "";
      form.email = "";
      form.company = "";
      form.consent = false;

      // Ya no necesitamos la descarga automática porque ahora es un formulario de contacto
    } else {
      throw new Error("Error en el envío del formulario");
    }
  } catch (error) {
    console.error("Error:", error);
    alert(
      "Hubo un error al procesar tu solicitud. Por favor intenta nuevamente."
    );
  } finally {
    isSubmitting.value = false;
  }
};
</script>
